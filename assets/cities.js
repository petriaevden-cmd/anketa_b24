/**
 * getCityTZ(cityName) — UTC-смещение по названию города (или null).
 *
 * Принимает как полный ключ "Самара, Самарская область",
 * так и просто "Самара" — нечёткий поиск по началу ключа.
 *
 * Если несколько городов с одинаковым названием — возвращает первый
 * найденный (тот, который стоит выше в объекте). Для точного определения
 * нужно передавать полный ключ с регионом.
 */
function getCityTZ(cityName) {
  if (!cityName) return null;
  const name = cityName.trim();

  // 1. Точное совпадение
  if (CITIES_TZ[name] !== undefined) return CITIES_TZ[name];

  const lower = name.toLowerCase();

  // 2. Ключ начинается с введённого названия (например "Самара" → "Самара, Самарская область")
  const startMatch = Object.keys(CITIES_TZ).find(k => k.toLowerCase().startsWith(lower + ',') || k.toLowerCase() === lower);
  if (startMatch) return CITIES_TZ[startMatch];

  // 3. Нечёткий поиск: ключ содержит введённое название
  const fuzzyMatch = Object.keys(CITIES_TZ).find(k => k.toLowerCase().includes(lower));
  return fuzzyMatch ? CITIES_TZ[fuzzyMatch] : null;
}

/**
 * getCityList() — отсортированный массив строк для автодополнения.
 * Формат каждого элемента: "Город, Регион   UTC+N"
 */
function getCityList() {
  return Object.keys(CITIES_TZ).sort().map(function (key) {
    return key + '   UTC+' + CITIES_TZ[key];
  });
}

